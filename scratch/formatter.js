
const fs = require("fs");
let text = fs.readFileSync("Apis-Results.md", "utf-8");

// Add main header
text = text.replace("??? ????? ????? ???? ????? ?? apis ???", "# ?? ????? ????? ???????? ??? APIs (Promoo Backend)\n\n??? ????? ????? ???? ????? ?? apis ???");

// Wrap URLs
text = text.replace(/(\{\{baseUrl\}\}.*)/g, "\n### ?? `$1`\n");

// Formatting notes
text = text.replace(/??? ?? api ????? ?? ????.*/g, (match) => `> ?? **??????:** ${match}\n`);
text = text.replace(/???? ????? ?????? ?? profile 1.*/g, (match) => `---\n\n## ?? Profiles (??????? ???????)\n\n> ?? **??????:** ${match}\n`);
text = text.replace(/\{\{baseUrl\}\}\/profiles\/me\/verify-request ??? ????.*/g, (match) => `\n> ? **????:** ${match}\n\n---\n\n## ?? Follows (?????????)\n`);
text = text.replace(/### ?? `\{\{baseUrl\}\}\/categories`/g, "---\n\n## ??? Categories (?????????)\n\n### ?? `{{baseUrl}}/categories`");
text = text.replace(/### ?? `\{\{baseUrl\}\}\/search/g, "---\n\n## ?? Search (?????)\n\n### ?? `{{baseUrl}}/search");

// Insert Auth header
text = text.replace(/### ?? `\{\{baseUrl\}\}\/auth\/register\/email`/g, "---\n\n## ?? Auth (???? ????????)\n\n### ?? `{{baseUrl}}/auth/register/email`");

// Wrap JSON blocks
let lines = text.split("\n");
let inJson = false;
let openBraces = 0;
let newLines = [];

for (let i = 0; i < lines.length; i++) {
  let line = lines[i];
  
  if (!inJson && line.trim() === "{") {
     inJson = true;
     openBraces = 1;
     newLines.push("```json");
     newLines.push(line);
  } else if (inJson) {
     newLines.push(line);
     // count braces
     let openCount = (line.match(/\{/g) || []).length;
     let closeCount = (line.match(/\}/g) || []).length;
     openBraces += openCount;
     openBraces -= closeCount;
     
     if (openBraces === 0) {
        inJson = false;
        newLines.push("```");
     }
  } else {
     newLines.push(line);
  }
}

// Write back
fs.writeFileSync("Apis-Results.md", newLines.join("\n"));
console.log("Formatted successfully!");

